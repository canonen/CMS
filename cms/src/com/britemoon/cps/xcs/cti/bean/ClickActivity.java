package com.britemoon.cps.xcs.cti.bean;


public class ClickActivity  {

     private String clickId;
     private String typeId;
     private String linkId;
     private String contentType;
     private String recipId;
     private String campId;
     private String remoteIp;
     private String clickTime;
     private String recipEmailAddr;
     private String linkHref;

     public ClickActivity () {
     }

     public String getClickId() {
          return clickId;
     }
     public void setClickId(String aClickId) {
          clickId = aClickId;
     }
     
     public String getTypeId() {
          return typeId;
     }
     public void setTypeId(String aTypeId) {
          typeId = aTypeId;
     }
     
     public String getLinkId() {
          return linkId;
     }
     public void setLinkId(String aLinkId) {
          linkId = aLinkId;
     }
     
     public String getContentType() {
          return contentType;
     }
     public void setContentType(String aContentType) {
          contentType = aContentType;
     }
     
     public String getRecipId() {
          return recipId;
     }
     public void setRecipId(String aRecipId) {
          recipId = aRecipId;
     }
     
     public String getCampId() {
          return campId;
     }
     public void setCampId(String aCampId) {
          campId = aCampId;
     }
     
     public String getRemoteIp() {
          return remoteIp;
     }
     public void setRemoteIp(String aRemoteIp) {
          remoteIp = aRemoteIp;
     }
     
     public String getClickTime() {
          return clickTime;
     }
     public void setClickTime(String aClickTime) {
          clickTime = aClickTime;
     }
     
     public String getRecipEmailAddr() {
          return recipEmailAddr;
     }
     public void setRecipEmailAddr(String aRecipEmailAddr) {
          recipEmailAddr = aRecipEmailAddr;
     }
     
     public String getLinkHref() {
          return linkHref;
     }
     public void setLinkHref(String aLinkHref) {
          linkHref = aLinkHref;
     }
     


    
}